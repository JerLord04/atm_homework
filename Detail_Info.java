public class Detail_Info {
    String firstName;
    String lastName;
    String numberAccount;
    String tel;
    String email;
    String username;
    String password;
    Float wallet_money;
    
    public Detail_Info(
        String firstName,
        String lastName,
        String numberAccount,
        String tel,
        String email,
        String username,
        String password,
        Float wallet_money
    ){
        this.firstName = firstName;
        this.lastName = lastName;
        this.numberAccount = numberAccount;
        this.tel = tel;
        this.email = email;
        this.username = username;
        this.password = password;
        this.wallet_money = wallet_money;
    }

}
